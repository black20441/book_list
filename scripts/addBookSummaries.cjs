// 为书籍添加简介
function addBookSummaries() {
  console.log('开始为书籍添加简介...');
  
  for (let i = 0; i < books.length; i++) {
    const book = books[i];
    const isbn = book.isbn || book.ISBN || book['ISBN'];
    
    if (isbn && mockSummaries[isbn]) {
      console.log(`为第 ${i + 1}/${books.length} 本书添加简介: ${book.title || book['书名']}`);
      book.summary = mockSummaries[isbn];
    } else {
      console.log(`第 ${i + 1}/${books.length} 本书: ${book.title || book['书名']} - 暂无简介`);
      book.summary = '暂无简介';
    }
  }
  
  // 保存更新后的数据
  fs.writeFileSync(booksPath, JSON.stringify(books, null, 2));
  console.log('书籍简介添加完成，已更新books.json文件');
}

// 运行脚本
addBookSummaries();
